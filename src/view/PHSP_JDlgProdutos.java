    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
      // TODO add your handling code here:
        PhspProdutosDAO phspProdutosDAO = new PhspProdutosDAO();
        Phspprodutos phspprodutos = viewBean();
        if (incluir == true) {
            phspProdutosDAO.insert(phspprodutos);
            //produtosDAO.insert( viewBean() );
        } else {
            phspProdutosDAO.update(phspprodutos);
            //produtosDAO.update( viewBean() );
        }
        
      Util.habilitar(false, jTxtCodigo,jTxtNome, jTxtCategoria,jTxtPlataforma, jFmtLancamento
        , jTxtPreco, jTxtDescricao,jBtnIncluir, jBtnConfirmar,jBtnCancelar);
        
         Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir13, jBtnPesquisar);
         
              Util.limpar(jTxtCodigo,jTxtNome, jTxtCategoria,jTxtPlataforma, jFmtLancamento
        , jTxtPreco, jTxtDescricao);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

       
