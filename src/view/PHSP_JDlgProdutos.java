    public void beanView(Phspprodutos phspprodutos) {
        jTxtCodigo.setText(Util.intToStr(phspprodutos.getPhspIdProdutos()));
        jTxtNome.setText(phspprodutos.getPhspNome());
        jTxtCategoria.setText(phspprodutos.getPhspCategoria());
        jTxtPlataforma.setText(phspprodutos.getPhspPlataforma());
        jFmtLancamento.setText(Util.dateToStr(phspprodutos.getPhspLancamento()));
        jTxtPreco.setText(Util.doubleToStr(phspprodutos.getPhspPreco()));
        jTxtDescricao.setText(phspprodutos.getPhspDescricao());

    }

    public Phspprodutos viewBean() {
        Phspprodutos phspprodutos = new Phspprodutos();
        int codigo = Util.strToInt(jTxtCodigo.getText());
        phspprodutos.setPhspIdProdutos(codigo);

        phspprodutos.setPhspNome(jTxtNome.getText());
        phspprodutos.setPhspCategoria(jTxtCategoria.getText());
        phspprodutos.setPhspPlataforma(jTxtPlataforma.getText());
        phspprodutos.setPhspLancamento(Util.strToDate(jFmtLancamento.getText()));
        phspprodutos.setPhspPreco(Util.strToDouble(jTxtPreco.getText()));
        phspprodutos.setPhspDescricao(jTxtDescricao.getText());
        return phspprodutos;
    }

