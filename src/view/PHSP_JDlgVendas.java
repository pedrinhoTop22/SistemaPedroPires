    public void beanView(Phspvendas phspvendas) {
        jTxtCodigo.setText(Util.intToStr(phspvendas.getPhspIdVendas()));
        jCboClientes.setSelectedItem(phspvendas.getPhspclientes());
        jCboUsuarios.setSelectedItem(phspvendas.getPhspusuarios());
        jFmtDataVenda.setText(Util.dateToStr(phspvendas.getPhspDataVenda()));
        jTxtTotal.setText(Util.doubleToStr(phspvendas.getPhspTotal()));
        jTxtFormaPagamento.setText(Util.doubleToStr(phspvendas.getPhspTotal()));
        jTxtDesconto.setText(Util.doubleToStr(phspvendas.getPhspTotal()));

    }

